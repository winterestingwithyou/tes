    //Method untuk Konversi ID menjadi Nomor
    private int konversiid(String input) {
        int result = 0;
        if (input.length() > 1) {
            String substring = input.substring(1);

            try {
                result = Integer.parseInt(substring);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Tidak dapat mengonversi ke integer", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Input terlalu pendek", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        return result;
    }
    
