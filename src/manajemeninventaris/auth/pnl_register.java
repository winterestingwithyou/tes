    private void btnMasukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMasukActionPerformed
        String iduser = idbaru();
        String username = txtUsername.getText();
        String password;
        String role = "Karyawan";
        
        //Cek bahwa Password dan Konfirmasi Password sama
        if(!txtPassword.getText().equals(txtKonfirmasiPassword.getText())){
            JOptionPane.showMessageDialog(this, "Harap Periksa Password Kembali", "Error 2", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            password = PasswordUtils.hashPassword(txtPassword.getText());
        }
        
        //Memasukkan Data User Baru
        try{
            User user = new User();
            user.setId(iduser);
            user.setUsername(username);
            user.setPassword(password);
            user.setRole(role);
            new UserController().store(user);
            
            Session session = Session.getInstance();
            session.setID(iduser);
            session.setUsername(username);
            session.setRole(role);
            session.setContact(null);
            session.setCreated(LocalDate.now().toString());
            JOptionPane.showMessageDialog(this, "Register Berhasil!");
            
            JFrame Form = new MainJFrame();
            Form.setVisible(true);
            form.dispose();
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Register Gagal", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnMasukActionPerformed
    
    //Method untuk mendapatkan ID User Baru berdasarkan nomor
    public String idbaru() {
        String iduser = "";
        try(java.sql.Connection conn=(Connection)config.configDB()){
            String query = "SELECT Id_user FROM user ORDER BY Id_user DESC LIMIT 1";
            PreparedStatement stmt = conn.prepareStatement(query);
            ResultSet rs = stmt.executeQuery();
            
            if (rs.next()) {
                int iduserbaru = konversiid(rs.getString("Id_user")) + 1;
                iduser = String.format("U%02d", iduserbaru);
            } else {
                iduser = "U01";
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error 1", JOptionPane.ERROR_MESSAGE);
        }
        return iduser;
    }
    
