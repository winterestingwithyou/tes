    // Mengisi Comobo Box ID Barang berdasarkan ID pada Tabel Barang
    private void isicbidbrg() {        
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        
        try{
            String sql = "select Id_barang from barang";
            java.sql.Connection conn=(Connection)config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while (res.next()) {
                model.addElement(res.getString("Id_barang"));
            }
        } catch (Exception e) { 
        }
  
        cbidbrg.setModel(model);
        cbidbrg.setSelectedItem(null);
    }
    
