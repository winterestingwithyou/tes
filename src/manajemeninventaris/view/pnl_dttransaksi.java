    //Menyesuaikan Button Sesuai Role
    private void roleButton() {
        Session session = Session.getInstance();
        switch(session.getRole()){
            case "Supervisor" -> Role.setNoAccessButton("Supervisor", btnTambah, btnEdit, btnHapus);
            default -> {
            }
        }
    }
    
    //Menambah Data Transaksi Baru
    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        //Mengambil data dari komponen
        String id = txtidtrans.getText();
        String idBrg = cbidbrg.getSelectedItem().toString();
        String idUser = Session.getInstance().getID();
        String tipe = cbtipe.getSelectedItem().toString();
        int jumlah = Integer.parseInt(txtjml.getText());
              
        try {
            //Menambah data ke database
            Transaksi transaksi = new Transaksi();
            transaksi.setId(id);
            transaksi.setIdBarang(idBrg);
            transaksi.setIdUser(idUser);
            transaksi.setTipe(tipe);
            transaksi.setJumlah(jumlah);
            new TransaksiController().store(transaksi);
            
            //Pesan jika berhasil
            JOptionPane.showMessageDialog(null, "Berhasil menambahkan Data Transaksi");
        } catch(Exception e) {
            //Pesan jika gagal
            JOptionPane.showMessageDialog(null, "Gagal : " + e.getMessage(), "Gagal menambahkan Data Transaksi", JOptionPane.ERROR_MESSAGE);
        }
        read();
        kosong();
    }//GEN-LAST:event_btnTambahActionPerformed

    //Menampilkan data dari database ke tabel
    private void read() {
        try {
            new TransaksiController().index(tblData);
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal : " + e.getMessage(), "Gagal menampilkan Transaksi", JOptionPane.ERROR_MESSAGE);
        }   
    }
   
