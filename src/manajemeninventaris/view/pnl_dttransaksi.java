    // Menampilkan Data ke Komponen Form
    private void tblDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataMouseClicked
        int baris = tblData.rowAtPoint(evt.getPoint());
        String idtrans =tblData.getValueAt(baris, 1).toString();
        txtidtrans.setText(idtrans);
        
        String idbrg = tblData.getValueAt(baris,2).toString();
        cbidbrg.setSelectedItem(idbrg);
        
        String tipe =tblData.getValueAt(baris, 4).toString();
        cbtipe.setSelectedItem(tipe);
        
        String jml = tblData.getValueAt(baris, 5).toString();
        txtjml.setText(jml);
    }//GEN-LAST:event_tblDataMouseClicked

